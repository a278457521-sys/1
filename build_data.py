def clean(text):
    return re.sub(r"\s+", " ", text).strip()


def extract_works(parts):
    text = " ".join(parts)
    matches = re.findall(r"代表作[：:]([^。]+)", text)
    works = []
    for match in matches:
        works.extend(re.split(r"[、，,；;]", match))
    return [clean(item) for item in works if clean(item)]


def strip_works(text):
    return clean(re.sub(r"代表作[：:][^。]+。?", "", text))


def content_length(item):
    return len(clean(item["background"] + item["features"] + item["impact"]).replace(" ", ""))


def ensure_noun_length(items):
    for item in items:
        if content_length(item) >= MIN_NOUN_LENGTH:
            continue
        supplement = NOUN_SUPPLEMENTS.get(item["title"])
        if not supplement:
            raise ValueError(f"Missing noun supplement: {item['title']} ({content_length(item)} chars)")
        item["impact"] = clean(item["impact"] + supplement)

    short_items = [(item["title"], content_length(item)) for item in items if content_length(item) < MIN_NOUN_LENGTH]
    if short_items:
        details = ", ".join(f"{title}={length}" for title, length in short_items)
        raise ValueError(f"Noun explanations below {MIN_NOUN_LENGTH} chars: {details}")


def ensure_works(items):
    missing = []
    for item in items:
        works = [clean(work) for work in item.get("works", []) if clean(work)]
        if not works or any("相关代表作品" in work for work in works):
            missing.append(item["title"])
            continue
        item["works"] = works
    if missing:
        raise ValueError("Missing representative works: " + "、".join(missing))


def parse_source():
    document = Document(SOURCE)
    items = []
    chapter = "未分类"
    current = None

    for paragraph in document.paragraphs:
        text = clean(paragraph.text)
        if not text:
            continue
        style = paragraph.style.name if paragraph.style else ""
        if style == "Heading 1":
            chapter = text
            continue
        is_topic = (
            style == "Normal"
            and paragraph.runs
            and paragraph.runs[0].bold is True
            and paragraph.runs[0].font.color.rgb is not None
            and str(paragraph.runs[0].font.color.rgb) == "003366"
        )
        if is_topic:
            current = {"title": text, "chapter": chapter, "parts": []}
            items.append(current)
        elif current and text.startswith(("【总】", "【分】")):
            current["parts"].append(text[3:])

    result = []
    for index, item in enumerate(items, start=1):
        parts = item.pop("parts")
        if not parts:
            continue
        works = extract_works(parts)
        features = strip_works(parts[1] if len(parts) > 1 else "")
        impact = strip_works(parts[-1] if len(parts) > 2 else "")
        embedded_impact = re.search(r"(?:影响|意义)[：:]", features)
        if embedded_impact:
            impact = clean(features[embedded_impact.end():] + impact)
            features = clean(features[:embedded_impact.start()])
        result.append({
            "id": f"topic-{index:03d}",
            "title": item["title"],
            "chapter": item["chapter"],
            "background": strip_works(parts[0]),
            "features": features,
            "impact": impact,
            "works": works or WORK_OVERRIDES.get(item["title"], []),
            "aliases": ALIASES.get(item["title"], []),
            "frequency": (
                "high" if item["title"] in HIGH_FREQUENCY
                else "medium" if item["title"] in MEDIUM_FREQUENCY
                else "normal"
            ),
            "image": IMAGES.get(item["title"]),
        })
    bauhaus = next((item for item in result if item["title"] == "包豪斯"), None)
    bauhaus_impact = next((item for item in result if item["title"] == "包豪斯的历史作用和影响"), None)
    if bauhaus and bauhaus_impact:
        bauhaus["impact"] = clean(
            bauhaus["impact"]
            + "从历史作用看，"
            + bauhaus_impact["features"]
            + bauhaus_impact["impact"]
        )
    return result


